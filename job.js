        // Function to format date
      function formatDate(dateString) {
    if (!dateString) return "Not specified";
    
    const options = { day: 'numeric', month: 'long', year: 'numeric' };
    const date = new Date(dateString);
    return date.toLocaleDateString('en-IN', options);
}

// Function to render job cards
function renderJobs(jobsToRender) {
    const container = document.getElementById('jobsContainer');
    container.innerHTML = '';
    
    if (jobsToRender.length === 0) {
        container.innerHTML = '<div class="no-results">No jobs found matching your criteria</div>';
        return;
    }
    
    jobsToRender.forEach(job => {
        const card = document.createElement('div');
        card.className = 'job-card';
        
        let statusBadge = '';
        let statusClass = '';
        
        if (job.status === 'active') {
            statusBadge = 'Active';
            statusClass = 'status-open';
        } else if (job.status === 'soon') {
            statusBadge = 'Closing Soon';
            statusClass = 'status-soon';
        } else if (job.status === 'closed') {
            statusBadge = 'Closed';
            statusClass = 'status-closed';
        }
        
        card.innerHTML = `
            <div class="job-header">
                <h3 class="job-title">${job.title}</h3>
                <div class="job-org">${job.org}</div>
                <div class="job-meta">
                    ${job.posts ? `<div class="meta-item"><i class="fas fa-users"></i> ${job.posts} Posts</div>` : ''}
                    <div class="meta-item"><i class="fas fa-map-marker-alt"></i> All India</div>
                </div>
            </div>
            <div class="job-body">
                <p class="job-details">${job.description}</p>
                ${statusBadge ? `<span class="status-badge ${statusClass}">${statusBadge}</span>` : ''}
            </div>
            <div class="job-footer">
                <div class="date-info">
                    <i class="far fa-calendar-alt"></i>
                    ${job.lastDate ? `Last Date: ${formatDate(job.lastDate)}` : 'Ongoing Process'}
                </div>
                ${job.applyLink ? `
                    <a href="${job.applyLink}" target="_blank" class="apply-btn">
                        <i class="fas fa-external-link-alt"></i> Apply
                    </a>` : ''
                }
            </div>
        `;
        
        container.appendChild(card);
    });
}

// Filter jobs based on status
function filterJobs(status) {
    if (status === 'all') {
        return jobs;
    }
    return jobs.filter(job => job.status === status);
}

// Search jobs
function searchJobs(query) {
    if (!query) return jobs;
    
    const lowerQuery = query.toLowerCase();
    return jobs.filter(job => 
        job.title.toLowerCase().includes(lowerQuery) || 
        job.org.toLowerCase().includes(lowerQuery) ||
        (job.description && job.description.toLowerCase().includes(lowerQuery))
    );
}

// Toggle mobile menu
const toggleBtn = document.getElementById('toggleBtn');
const navContainer = document.getElementById('navContainer');

toggleBtn.addEventListener('click', () => {
    navContainer.classList.toggle('active');
    
    if (navContainer.classList.contains('active')) {
        toggleBtn.innerHTML = '<i class="fas fa-times"></i>';
    } else {
        toggleBtn.innerHTML = '<i class="fas fa-bars"></i>';
    }
});

// Close menu when clicking on links
document.querySelectorAll('.nav-links a').forEach(link => {
    link.addEventListener('click', () => {
        navContainer.classList.remove('active');
        toggleBtn.innerHTML = '<i class="fas fa-bars"></i>';
    });
});

// Event listeners for job page
document.addEventListener('DOMContentLoaded', () => {
    renderJobs(jobs);

    const filterButtons = document.querySelectorAll('.filter-btn');
    filterButtons.forEach(button => {
        button.addEventListener('click', () => {
            filterButtons.forEach(btn => btn.classList.remove('active'));
            button.classList.add('active');
            
            const filter = button.dataset.filter;
            const filteredJobs = filterJobs(filter);
            renderJobs(filteredJobs);
        });
    });

    const searchInput = document.getElementById('searchInput');
    searchInput.addEventListener('input', () => {
        const query = searchInput.value.trim();
        const activeFilter = document.querySelector('.filter-btn.active').dataset.filter;
        let results = jobs;

        if (query) {
            results = searchJobs(query);
        }

        if (activeFilter !== 'all') {
            results = results.filter(job => job.status === activeFilter);
        }

        renderJobs(results);
    });
});